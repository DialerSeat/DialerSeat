function relativeTime(iso: string | null | undefined): string {
  if (!iso) return '—'
  const then = new Date(iso).getTime()
  const now = Date.now()
  const diff = Math.max(0, now - then)
  const sec = Math.floor(diff / 1000)
  if (sec < 60) return 'just now'
  const min = Math.floor(sec / 60)
  if (min < 60) return `${min}m ago`
  const hr = Math.floor(min / 60)
  if (hr < 24) return `${hr}h ago`
  const day = Math.floor(hr / 24)
  if (day < 7) return `${day}d ago`
  const wk = Math.floor(day / 7)
  if (wk < 4) return `${wk}w ago`
  const mo = Math.floor(day / 30)
  if (mo < 12) return `${mo}mo ago`
  const yr = Math.floor(day / 365)
  return `${yr}y ago`
}

function LeadPreviewThumb({
  leads,
  totalLeads,
  emptyHint = 'NO LEADS UPLOADED',
  onClick,
  interactive = false,
  height = '100%',
}: {
  leads: Lead[]
  totalLeads: number
  emptyHint?: string
  onClick?: () => void
  interactive?: boolean
  height?: number | string
}) {
  if (!leads || leads.length === 0) {
    return (
      <div
        onClick={onClick}
        style={{
          flex: 1,
          height,
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'center',
          color: T.muted,
          fontSize: 10,
          letterSpacing: 2,
          fontFamily: FUTURA,
          fontWeight: 'bold',
          background: 'white',
          borderRadius: 4,
          border: `1px solid ${T.border}`,
          cursor: interactive ? 'pointer' : 'default',
        }}
      >
        {emptyHint}
      </div>
    )
  }

  const rows = leads.slice(0, 8)
  const extraKey = (() => {
    if (!rows[0]?.extra_data) return null
    const candidates = Object.keys(rows[0].extra_data).filter(k => {
      const v = rows[0].extra_data?.[k]
      return v && String(v).trim() && k.length < 20
    })
    return candidates[0] || null
  })()

  return (
    <div
      onClick={onClick}
      style={{
        flex: 1,
        height,
        overflow: 'hidden',
        background: 'white',
        borderRadius: 4,
        border: `1px solid ${T.border}`,
        cursor: interactive ? 'pointer' : 'default',
        position: 'relative',
        display: 'flex',
        flexDirection: 'column',
      }}
    >
      <table style={{
        width: '100%',
        borderCollapse: 'collapse',
        fontSize: 9,
        fontFamily: 'monospace',
        color: T.text,
        tableLayout: 'fixed',
      }}>
        <thead>
          <tr style={{ background: T.surface, borderBottom: `1px solid ${T.border}` }}>
            <th style={thStyle}>NAME</th>
            <th style={thStyle}>PHONE</th>
            <th style={thStyle}>STATE</th>
            {extraKey && <th style={thStyle}>{extraKey.toUpperCase()}</th>}
          </tr>
        </thead>
        <tbody>
          {rows.map((lead, i) => (
            <tr key={lead.id} style={{
              borderBottom: `1px solid ${T.bg}`,
              background: i % 2 === 0 ? 'white' : T.bg,
            }}>
              <td style={tdStyle}>
                {lead.first_name} {lead.last_name}
              </td>
              <td style={tdStyle}>{lead.phone}</td>
              <td style={tdStyle}>{lead.state || ''}</td>
              {extraKey && (
                <td style={tdStyle}>
                  {String(lead.extra_data?.[extraKey] || '').slice(0, 16)}
                </td>
              )}
            </tr>
          ))}
        </tbody>
      </table>
      <div style={{
        position: 'absolute',
        bottom: 0, left: 0, right: 0,
        height: 28,
        background: 'linear-gradient(to bottom, rgba(255,255,255,0) 0%, rgba(255,255,255,0.95) 100%)',
        pointerEvents: 'none',
      }} />
      <div style={{
        position: 'absolute',
        bottom: 4,
        right: 6,
        fontSize: 9,
        color: 'white',
        fontFamily: 'monospace',
        fontWeight: 'bold',
        letterSpacing: 1,
        background: 'rgba(26, 26, 46, 0.92)',
        padding: '2px 6px',
        borderRadius: 2,
      }}>
        {totalLeads.toLocaleString()} {totalLeads === 1 ? 'LEAD' : 'LEADS'}
      </div>
    </div>
  )
}

